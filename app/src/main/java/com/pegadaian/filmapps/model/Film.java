package com.pegadaian.filmapps.model;

public class Film {
    private String kode;
    private String filmName;
    private String filmRating;
    private String filmDirector;
    private String filmGenre;
    private String filmDescription;
    private String filmRuntime;
    private String filmRelease;
    private String filmImg;

    public String getFilmName() {
        return filmName;
    }

    public void setFilmName(String filmName) {
        this.filmName = filmName;
    }

    public String getFilmRating() {
        return filmRating;
    }

    public void setFilmRating(String filmRating) {
        this.filmRating = filmRating;
    }

    public String getFilmDirector() {
        return filmDirector;
    }

    public void setFilmDirector(String filmDirector) {
        this.filmDirector = filmDirector;
    }

    public String getFilmGenre() {
        return filmGenre;
    }

    public void setFilmGenre(String filmGenre) {
        this.filmGenre = filmGenre;
    }

    public String getFilmDescription() {
        return filmDescription;
    }

    public void setFilmDescription(String filmDescription) {
        this.filmDescription = filmDescription;
    }

    public String getFilmRuntime() {
        return filmRuntime;
    }

    public void setFilmRuntime(String filmRuntime) {
        this.filmRuntime = filmRuntime;
    }

    public String getFilmRelease() {
        return filmRelease;
    }

    public void setFilmRelease(String filmRelease) {
        this.filmRelease = filmRelease;
    }

    public String getKode() {
        return kode;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }


    public String getFilmImg() {
        return filmImg;
    }

    public void setFilmImg(String filmImg) {
        this.filmImg = filmImg;
    }

}
